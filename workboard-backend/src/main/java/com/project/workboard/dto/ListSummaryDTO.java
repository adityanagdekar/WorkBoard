package com.project.workboard.dto;

public class ListSummaryDTO {
	private int id;
	private String name;

	public ListSummaryDTO(int id, String name) {
		super();
		this.id = id;
		this.name = name;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

}
